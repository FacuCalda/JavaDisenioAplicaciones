    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       String valor= txtNuevoValor.getText();
       try {
    double nuevoValor = Double.parseDouble(valor);
  
        if (nuevoValor <= 0) {
            JOptionPane.showMessageDialog(null, "Valor inválido. El precio debe ser igual o mayor a cero.", "Error", JOptionPane.ERROR_MESSAGE);
       
        }
   String nombre= lstParking.getSelectedValue();
    double promedioTarifa = controlador.obtenerPromedioTarifa(nombre);
        
        if (nuevoValor >= 2 * promedioTarifa) {
            JOptionPane.showMessageDialog(null, "Valor demasiado alto. El sistema no permite dispersión de precios por encima del 100%. Ingrese un valor menor a " + (2 * promedioTarifa) + ".", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
   controlador.ObtenerTarifa(nombre, nuevoValor);
  
} catch (NumberFormatException e) {
    // Maneja la excepción si el texto no puede ser parseado a un entero
    JOptionPane.showMessageDialog(null, "El valor ingresado no es un número válido", "Error", JOptionPane.ERROR_MESSAGE);
}
       
    }//GEN-LAST:event_jButton1ActionPerformed

