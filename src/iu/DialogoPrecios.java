    @Override
    public void cargarTabla(Parking p) {
    lblTitulo.setText(nombreParking);
       ArrayList<String> listaTarifas = new ArrayList<>();
        for (Map.Entry<TipoVehiculo, Double> entry : p.getTarifaHashmap().entrySet()) {
            listaTarifas.add(entry.getKey() + " " + entry.getValue());
        }
         
        String[] arrayTarifas = new String[listaTarifas.size()];
        arrayTarifas = listaTarifas.toArray(arrayTarifas);
        lstParking.setListData(arrayTarifas);
      
        
    }
    

